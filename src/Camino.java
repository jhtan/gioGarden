
public class Camino {
	private int distacia;
	private String tipo;
	
	public Camino() {
		
	}
	
	public int getDistacia() {
		return distacia;
	}
	public void setDistacia(int distacia) {
		this.distacia = distacia;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
}
